package fr.uge.cascadia.tile;
/**
 * The Habitat enum represents different types of habitats in the game.
 *
 */
public enum Habitat {
   /**
    * Represents a mountain.
    */
   Mountains,

   /**
    * Represents a forest.
    */
   Forests,

   /**
    * Represents an Prairies.
    */
   Prairies,

   /**
    * Represents a Wetlands.
    */
   Wetlands,

   /**
    * Represents a Rivers.
    */
   Rivers

}