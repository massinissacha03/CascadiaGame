package fr.uge.cascadia.animal;

/**
 * The Animal enum represents different types of animals in the game.
 *
 */
public enum Animal {
   /**
    * Represents a bear.
    */
   Bear,

   /**
    * Represents a fox.
    */
   Fox,

   /**
    * Represents an elk.
    */
   Elk,

   /**
    * Represents a salmon.
    */
   Salmon,

   /**
    * Represents a buzzard.
    */
   Buzzard
}